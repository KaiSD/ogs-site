function Физика(события)
{
    this.создать = function()
    {
        this.задано = {};
    };

    this.обработатьКлюч = function(ключ, путь, значение)
    {
        if (путь[0] != "физика")
        {
            return;
        }

        let имя = путь[1];
        let свойство = путь.slice(2).join(".");
        if (!this.задано[имя])
        {
            this.задано[имя] = {};
        }
        this.задано[имя][свойство] = значение;

        события.уведомить(`физика/${имя}`);
    };

    // Конструктор.
    this.создать();
};
